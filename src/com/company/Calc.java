package com.company;

public class Calc {
    // part 1
    public int add(int num1, int num2) {
        int sum = (num1 + num2);
        return sum;
    }

    public int subtract(int num1, int num2){
        int sub = (num1 - num2);
        return sub;
    }

    public int multiply(int num1, int num2){
        int mulit = (num1 * num2);
        return mulit;
    }

    public int divide(int num1, int num2){
        int div = (num1 / num2);
        return div;
    }
}
